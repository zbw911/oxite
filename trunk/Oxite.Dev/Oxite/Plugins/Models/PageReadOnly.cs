using System;

namespace Oxite.Plugins.Models
{
    public class PageReadOnly
    {
        public PageReadOnly(Oxite.Models.Page page)
        {
            if (page.Parent != null)
                Parent = new PageReadOnly(page.Parent);
            if (page.Creator != null)
                Creator = new UserReadOnly(page.Creator);
            Title = page.Title;
            Body = page.Body;
            State = (State)(byte)page.State;
            Slug = page.Slug;
            Created = page.Created;
            Modified = page.Modified;
            Published = page.Published;
            HasChildren = page.HasChildren;
        }

        public PageReadOnly Parent { get; private set; }
        public UserReadOnly Creator { get; private set; }
        public string Title { get; private set; }
        public string Body { get; private set; }
        public State State { get; private set; }
        public string Slug { get; private set; }
        public DateTime Created { get; private set; }
        public DateTime Modified { get; private set; }
        public DateTime? Published { get; private set; }
        public bool HasChildren { get; private set; }
    }
}
